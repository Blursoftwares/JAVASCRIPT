/*Basics of JavaScript
1. Variables: Storing Information
Variables are containers for storing information. In JavaScript, we use var, let, or const to declare variables.

var: The older way to declare variables, allows the value to be changed.
let: A more modern way to declare variables, also allows changes.
const: Once declared, its value cannot be changed.


*/
// Example 1: Storing a name in a variable
let name = "Alice"; // Stores the name "Alice"
console.log(name);  // Prints "Alice" in the console


let age = 10; // Stores the number 10
age = 11;     // Updates the variable to 11
console.log(age); // Prints 11 in the console
/*
Example 3: Using const for a value that wonâ€™t change

*/
const birthday = "January 1"; // Cannot change this value later
console.log(birthday);        // Prints "January 1" in the console

